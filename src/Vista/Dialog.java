    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if (getAction() == "upd") {
                this.employee = new MasterEmployee(Integer.valueOf(jTextField1.getText()), java.sql.Date.valueOf(jTextField2.getText()), jTextField3.getText(), jTextField4.getText(), jTextField5.getText(), java.sql.Date.valueOf(jTextField6.getText()));
                w.hiring.updateEmployee(getEmployee());
                JOptionPane.showMessageDialog(this,
                        "Registro actualizado con exito.");
            } else if (getAction() == "add") {
                this.employee = new MasterEmployee(Integer.valueOf(jTextField1.getText()), java.sql.Date.valueOf(jTextField2.getText()), jTextField3.getText(), jTextField4.getText(), jTextField5.getText(), java.sql.Date.valueOf(jTextField6.getText()));
                w.hiring.createEmployee(getEmployee());
                JOptionPane.showMessageDialog(this,
                        "Registro agregado con exito.");
            }
            this.w.populateTable();
            this.dispose();

        } catch (SQLException ex) {
            Logger.getLogger(Dialog.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

