    public boolean populateFields() {
        this.jTextField1.setText(Integer.toString(getEmployee().getEmp_no()));
        this.jTextField2.setText(getEmployee().getBirth_date().toString());
        this.jTextField3.setText(getEmployee().getFirst_name());
        this.jTextField4.setText(getEmployee().getLast_name());
        this.jTextField5.setText(getEmployee().getGender());
        this.jTextField6.setText(getEmployee().getHire_date().toString());
        return true;
    }

